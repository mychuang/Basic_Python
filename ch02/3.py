#輸入與輸出

a = input("Input an integer: ")
print(a)
print(type(a))

b = input("Input a string: ")
print(b)
print(type(b))

c = input("Input a float: ")
print(c)
print(type(c))

# 使用強制轉型

a = int(input("Input an integer: "))
print(a)
print(type(a))

b = input("Input a string: ")
print(b)
print(type(b))

c = float(input("Input a float: "))
print(c)
print(type(c))

