"""
每個變數都有自己的生命周期 (scope),
當一個變數被宣告時, 也決定這個變數存在的範圍
若變數宣告在函式中, 那此變數只能存活在該函式中
稱此變數為"區域變數"
"""

def f():
    a = 20
    print("show a in f(): ", a)

a = 10
print(a)

f()
print(a)

print("###################################")

"""
若想讓變數在各個函式中共用, 則可以使用 global 修飾符
這類型的變數稱為全域變數
"""

def f2():
    global b
    b = b + 10
    print("show b in f2(): ", b)

b = 5

f2()
print(b)