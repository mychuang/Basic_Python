a = 5
b = int(input("Input b (0-90):"))

# 關係運算子

print(f"a == b ?  {a==b} ");
print(f"a > b ?  {a>b} ");
print(f"a >= b ? {a>=b} ");
print(f"a < b ?  {a<=b} " );
print(f"a <= b ?  {a<=b} ");
print(f"a != b ? {a!=b} ");

# 邏輯運算子: and

print(f"b>=0 && b<30 ? {b>=0 and b<30} ");
print(f"b>=30 && b<60 ? {b>=30 and b<60} ");
print(f"b>=60 && b<90 ? {b>=60 and b<90} ");

# 邏輯運算子: or
print(f"b>=0 or b<30 ?  {b>=0 or b<30} ");
print(f"b>=30 or b<60 ?  {b>=30 or b<60}");
print(f"b>=60 or b<90 ?  {b>=60 or b<90}");
